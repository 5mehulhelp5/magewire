declare(strict_types=1);

namespace Magewirephp\Magewire\Features\SupportMagewireViewInstructions\Concern;

trait WithNaming
{
    private ?string $name = null;

    public function withName(string $name): static
    {
        $this->name = $name;

        return $this;
    }

    public function getName(): ?string
    {
        return $this->name;
    }
}
